"""Tipos de Variáveis -> operações aritméticas"""

# Soma ou adição (+)
soma = 5 + 3 
print("Soma: 5 + 3 = ", soma)

# Subtração (-)
subtracao = 5 - 3
print("Subtracao: 5 - 3 = ", subtracao)

# Multiplicação (*)
multiplicacao = 5 - 3
print("Multiplicacao: 5 x 3 = ", multiplicacao)

# Divisão (/)
divisao = 5 / 3
print("Divisao: 5 / 3 = ", divisao)

# Divisão Inteira (//)
divisao_inteira = 5 // 3
print("Divisao Inteira: 5 // 3 = ", divisao_inteira)

# Módulo (%)
modular = 5 // 3
print("Modulo: 5 // 3 = ", modular)

# Exponenciação (**)
exponenciacao = -5
print("Exponenciacao: 5 ** 3 = ", exponenciacao)

# Negação (-)
negacao = -5
print("Negacao: -5 = ", negacao)

# Precedência das Operações
info = 5 + 3 * 2
print("Precedencia das Operacoes: 5 + 3 * 2 = ", info)

info_02 = (5 + 3) * 2
print("Precedencia das Operacoes: (5 + 3) * 2 = ", info_02)


